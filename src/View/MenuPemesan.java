    private void cariBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBTNActionPerformed
        String namaresto = cariRestoInput.getText();
        
        if (namaresto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap masukan nama restoran yang ingin dicari");
        } else {
            namaRestoDisplay.setText(namaresto);
            Restoran newResto = restoCNTRL.getRestoByNama(namaresto);
            if (newResto == null) {
               JOptionPane.showMessageDialog(this, "restoran tidak ada"); 
            } else {
                loadDaftarMejaByNamaResto(namaresto, newResto.getIdRestoran());
            }
        }

    }//GEN-LAST:event_cariBTNActionPerformed

    private void daftarMejaRestoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_daftarMejaRestoMouseClicked
        int selectedRow = daftarMejaResto.getSelectedRow();
        
        if (selectedRow != -1) {
            int idMeja = (int) modelDaftarMeja.getValueAt(selectedRow, 0);
            int idResto = (int) modelDaftarMeja.getValueAt(selectedRow, 1);
            int jumlahKursi = (int) modelDaftarMeja.getValueAt(selectedRow, 2);
            String status = modelDaftarMeja.getValueAt(selectedRow, 3).toString();
            String namaResto = cariRestoInput.getText();
            
            BookingMeja bookMeja = new BookingMeja();
            bookMeja.BookingMeja(namaResto, status, jumlahKursi, idMeja, idResto);
            bookMeja.setVisible(true);
        }
    }//GEN-LAST:event_daftarMejaRestoMouseClicked

    private void RefreshBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshBTNActionPerformed
        daftarRiwayatBook.removeAll();
        int rows = modelHistoryBook.getRowCount();
        for(int i = rows - 1; i >= 0; i--){
            modelHistoryBook.removeRow(i);
        }
        
        loadRiawayatBooking(SesiLogin.getId());
    }//GEN-LAST:event_RefreshBTNActionPerformed

