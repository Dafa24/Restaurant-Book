    public void loadDaftarMejaByNamaResto(String namaresto, int idResto){
        List<DaftarMeja> dataMeja = dafMejaCNTRL.getAllDaftarMeja(idResto);
        
        for(DaftarMeja dafmeja : dataMeja){
            Object[] rowdatameja = {
                dafmeja.getIdMeja(),
                dafmeja.getIdResto(),
                dafmeja.getJumlahKursi(),
                dafmeja.getStatus(),
            };
            modelDaftarMeja.addRow(rowdatameja);
        }
    }
    
    public void loadRiawayatBooking(int idAkun){
        List<HistoryBooking> dataHistory = historyCNTRL.getHistoryBookByIdUser(idAkun);
        
        for(HistoryBooking hisbook : dataHistory){
            Object[] rowdatahistory = {
                hisbook.getIdResto(),
                hisbook.getNamaRestoran(),
                hisbook.getIdMeja(),
                hisbook.getWaktuDatang(),
                hisbook.getStatus()
            };
            modelHistoryBook.addRow(rowdatahistory);
        }
    }

