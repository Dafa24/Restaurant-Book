    public void loadLaporanBooking(){
        List<HistoryBooking> dataBooking = hisbookCNTRL.getAllHistoryBooking();
        
        for(HistoryBooking hisbook : dataBooking){
            Object[] rowData = {
                hisbook.getIdHistoryBooking(),
                hisbook.getNama_pemesan(),
                hisbook.getNamaRestoran(),
                hisbook.getWaktuDatang(),
                hisbook.getStatus()
            };
            ModelLaporanBooking.addRow(rowData);
        }
    }
    
    public void loadDataMejaResto(int idresto){
        List<DaftarMeja> dataMeja = dafmejaCNTRL.getAllDaftarMeja(idresto);
        
        for(DaftarMeja dafmeja : dataMeja){
            Object[] rowdatameja = {
                dafmeja.getIdMeja(),
                dafmeja.getIdResto(),
                dafmeja.getJumlahKursi(),
                dafmeja.getStatus(),
            };
            modelDataResto.addRow(rowdatameja);
        }
   }
    

