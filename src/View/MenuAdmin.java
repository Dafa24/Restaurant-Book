    private void CariBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariBTNActionPerformed
        String namaresto = namaRetsoInput.getText();
        
        Restoran newResto = restoCNTRL.getRestoByNama(namaresto);
        if (newResto != null) {
            idRestoDisplay.setText(String.valueOf(newResto.getIdRestoran()));
            namaRestoDisplay.setText(newResto.getNamaResto());
            alamatRestoDisplay.setText(newResto.getAlamat());
            loadDataMejaResto(newResto.getIdRestoran());
        } else {
            JOptionPane.showMessageDialog(null, "Restoran tidak ditemukan");
        }
        
    }//GEN-LAST:event_CariBTNActionPerformed

